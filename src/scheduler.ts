async function main() {
  console.log('‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó');
  console.log('‚ïë       üöÄ Twitch VOD Downloader Scheduler Started        ‚ïë');
  console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n');

  console.log('üìÖ Schedule Configuration:');
  console.log(`   ‚Ä¢ Download check: ${DOWNLOAD_SCHEDULE}`);
  console.log(`   ‚Ä¢ Cleanup:        ${CLEANUP_SCHEDULE}\n`);

  // Validate cron expressions
  if (!cron.validate(DOWNLOAD_SCHEDULE)) {
    console.error(`‚ùå Invalid DOWNLOAD_SCHEDULE cron expression: ${DOWNLOAD_SCHEDULE}`);
    process.exit(1);
  }
  if (!cron.validate(CLEANUP_SCHEDULE)) {
    console.error(`‚ùå Invalid CLEANUP_SCHEDULE cron expression: ${CLEANUP_SCHEDULE}`);
    process.exit(1);
  }

  // Initialize directories
  await initializeDirectories();

  // Schedule download check
  cron.schedule(DOWNLOAD_SCHEDULE, async () => {
    await runScheduledDownload();
  });

  // Schedule cleanup
  cron.schedule(CLEANUP_SCHEDULE, async () => {
    await runScheduledCleanup();
  });

  console.log('‚úÖ Scheduler is running. Press Ctrl+C to stop.\n');

  // Run immediately on startup (optional)
  if (process.env.RUN_ON_STARTUP === 'true') {
    console.log('üèÉ Running initial check on startup...\n');
    await runScheduledDownload();
  }

  // Keep the process alive
  process.on('SIGINT', () => {
    console.log('\n\nüõë Scheduler stopped by user');
    process.exit(0);
  });

  process.on('SIGTERM', () => {
    console.log('\n\nüõë Scheduler stopped');
    process.exit(0);
  });
}

main().catch((error) => {
  console.error('‚ùå Fatal error in scheduler:', error);
  process.exit(1);
});
